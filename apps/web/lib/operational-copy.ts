export function toIntlLocale(locale: DashboardLocale) {
  return intlLocales[locale] ?? "en-US"
}

export function resolveDashboardLocale(locale: string): DashboardLocale {
  return locale === "tr" || locale === "de" || locale === "ru" ? locale : "en"
}

const normalizedTables = new Map<DashboardLocale, Map<string, string>>()

function normalizeDashboardLookupKey(value: string) {
  return value
    .trim()
    .toLocaleLowerCase("tr-TR")
    .replace(/ı/g, "i")
    .replace(/ğ/g, "g")
    .replace(/ü/g, "u")
    .replace(/ş/g, "s")
    .replace(/ö/g, "o")
    .replace(/ç/g, "c")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, " ")
}

function getNormalizedTable(locale: DashboardLocale) {
  const cached = normalizedTables.get(locale)
  if (cached) return cached

  const normalized = new Map<string, string>()
  for (const sourceTable of [
    dashboardTextOverrides[locale],
    portalWorkflowText[locale],
    tables[locale],
  ]) {
    for (const [source, translated] of Object.entries(sourceTable)) {
      const key = normalizeDashboardLookupKey(source)
      if (!normalized.has(key)) normalized.set(key, translated)
    }
  }

  normalizedTables.set(locale, normalized)
  return normalized
}

export function localizeDashboardText(
  value: string | null | undefined,
  locale: DashboardLocale
) {
  if (!value) return value ?? ""
  const override =
    dashboardTextOverrides[locale][value] ??
    dashboardTextOverrides[locale][value.trim()] ??
    portalWorkflowText[locale][value] ??
    portalWorkflowText[locale][value.trim()]
  // Brand/development names (e.g. "New Level Premium") stay verbatim in every locale.
  if (override) return pinProperNouns(override)
  const businessCopy = localizeBusinessCopy(value, locale)
  return pinProperNouns(
    tables[locale][value] ??
    tables[locale][value.trim()] ??
    getNormalizedTable(locale).get(normalizeDashboardLookupKey(value)) ??
    (businessCopy !== value ? businessCopy : value)
  )
}

export function localizeDashboardTextPart(
  value: string | null | undefined,
  locale: DashboardLocale
) {
  if (!value) return value ?? ""

  const exact = localizeDashboardText(value, locale)
  if (exact !== value) return exact

  const withOverrides = [
    ...Object.entries(dashboardTextOverrides[locale]),
    ...Object.entries(portalWorkflowText[locale]),
  ]
    .sort(([left], [right]) => right.length - left.length)
    .reduce((current, [source, target]) => {
      if (source.length < 4) return current
      return current.split(source).join(target)
    }, value)

  const localized = withOverrides
    .replace(/satış planı/g, localizeDashboardText("satış planı", locale))
    .replace(/Malik kaydı bekliyor/g, localizeDashboardText("Malik kaydı bekliyor", locale))
    .replace(/Yasal takip ve erişim kısıtı/g, localizeDashboardText("Yasal takip ve erişim kısıtı", locale))
    .replace(/Telefon \+ WhatsApp tahsilat/g, localizeDashboardText("Telefon + WhatsApp tahsilat", locale))
    .replace(/\baksiyonlari\b/g, localizeDashboardText("Aksiyonlar", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bhazirla\b/g, locale === "tr" ? "hazırla" : localizeDashboardText("Hazır", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bgorev\b/g, locale === "tr" ? "görev" : localizeDashboardText("Görev", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bkanit\b/g, locale === "tr" ? "kanıt" : "evidence")
    .replace(/\bborc\b/g, locale === "tr" ? "borç" : localizeDashboardText("Borç", locale).toLocaleLowerCase(toIntlLocale(locale)))

  // Keep brand/development names verbatim after all substring localization.
  return pinProperNouns(localized)
}

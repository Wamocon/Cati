const tables: Record<DashboardLocale, TranslationTable> = {
  tr,
  en,
  de,
  ru,
}

const intlLocales: Record<DashboardLocale, string> = {
  tr: "tr-TR",
  en: "en-US",
  de: "de-DE",
  ru: "ru-RU",
}

export function toIntlLocale(locale: DashboardLocale) {
  return intlLocales[locale] ?? "en-US"
}

export function resolveDashboardLocale(locale: string): DashboardLocale {
  return locale === "tr" || locale === "de" || locale === "ru" ? locale : "en"
}

export function localizeDashboardText(
  value: string | null | undefined,
  locale: DashboardLocale
) {
  if (!value) return value ?? ""
  return tables[locale][value] ?? value
}

export function localizeDashboardTextPart(
  value: string | null | undefined,
  locale: DashboardLocale
) {
  if (!value) return value ?? ""

  const exact = localizeDashboardText(value, locale)
  if (exact !== value) return exact

  return value
    .replace(/\baksiyonlari\b/g, localizeDashboardText("Aksiyonlar", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bhazirla\b/g, locale === "tr" ? "hazırla" : localizeDashboardText("Hazır", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bgorev\b/g, locale === "tr" ? "görev" : localizeDashboardText("Görev", locale).toLocaleLowerCase(toIntlLocale(locale)))
    .replace(/\bkanit\b/g, locale === "tr" ? "kanıt" : "evidence")
    .replace(/\bborc\b/g, locale === "tr" ? "borç" : localizeDashboardText("Borç", locale).toLocaleLowerCase(toIntlLocale(locale)))
}
